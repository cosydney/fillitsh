#include <stdlib.h>
#include <string.h>
#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char			*dup;
	unsigned int	i;

	dup = (char *)malloc(sizeof(*dup) * (n + 1));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (i < n)
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
